import { Schema } from "mongoose";

const userSchema = Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ["admin", "user"],
      default: "user",
    },
  },
  { discriminatorKey: "kind", timestamps: true },
);

export { userSchema };
